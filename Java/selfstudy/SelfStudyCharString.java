class SelfStudyCharString {
	public static void main( String[] args) {
		System.out.println('H');				// character
		System.out.println("Hello");			// string
//		System.out.println('Hello');			''(quotation marks) can not use for a string
		System.out.println("H");				// ""(double quotation marks) is for a string. A character can be a string but requires more memory.
		System.out.println("Hello" + "JAVA");		// string can add
		System.out.println(1 + 1);				// add number
		System.out.println("1" + "1");			// add string
		System.out.println("Esther said \"Hello JAVA World!!!\""); //  if quotation marks are a string then add  \(backslash) in front of ""
		System.out.println("Esther said \n\"Hello \tJAVA\tWorld!!!\""); // \n line change, \t automatic word spacing
	}
}
