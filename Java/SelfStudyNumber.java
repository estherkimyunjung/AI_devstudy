class SelfStudyNumber {
	public static void main(String[] args) {
		System.out.println(1 + 2);				//add integer
		System.out.println(2 - 1);				//subtraction
		System.out.println(1.2 + 1.3);			//add real number
		System.out.println(2 * 5);				//multiplication
		System.out.println(6 / 2);				//division
		System.out.println(7 / 2);
		System.out.println(7 % 2);				//remainder
	}
}
